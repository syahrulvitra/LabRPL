    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String npm = jTextField1.getText();
        
        try {
            if (!npm.isEmpty()) {
                jpa.destroy(npm);
                JOptionPane.showMessageDialog(null, "Data berhasil dihapus");
                resetForm();
                showData();
            } else {
                JOptionPane.showMessageDialog(null, "Pilih data yang ingin dihapus");
            }
        } catch (Exception e) {
            Logger.getLogger(e.getMessage()).log(Level.SEVERE, null, e);
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int column = 0;
        int row = jTable1.getSelectedRow();
        String value = jTable1.getModel().getValueAt(row, column).toString();
        System.out.println("Selected row: " + value);
        
        MahasiswaModel mhs = jpa.findMahasiswaModel(value);
        
        jButton4.setEnabled(false);
        jTextField1.setEditable(false);
        jTextField1.setText(mhs.getNpm());
        jTextField2.setText(mhs.getNama());
        jTextField3.setText(mhs.getKelas());
        jTextField4.setText(mhs.getAlamat());
    }//GEN-LAST:event_jTable1MouseClicked

