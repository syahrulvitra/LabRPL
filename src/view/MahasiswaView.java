    private void showData() {
        String[] kolom = {"NPM", "Nama", "Kelas", "Alamat"};
        Object[][] objData = new Object[jpa.getMahasiswaModelCount()][4];
        int i = 0;
        
        for (MahasiswaModel mhs : jpa.findMahasiswaModelEntities()) {
            String[] arrData = {mhs.getNpm(), mhs.getNama(), mhs.getKelas(), mhs.getAlamat()};
            objData[i] = arrData;
            
            i++;
        }
        
        DefaultTableModel jTable1 = new DefaultTableModel(objData, kolom){
            @Override
            public boolean isCellEditable(int rowIndex, int colIndex){
                return false;
            }
        };
    }
